# String Methods

"""Strings are imutable  in Python, meaning once created, they cannot be changed.
However, you can create new strings based on existing ones using various string methods."""







# 1. capitalize()
string = "Hello, World!"
print(string.capitalize())  # Output: Hello, world!



# 2. casefold()
print(string.casefold())  # Output: hello, world!




# 3. center()
print(string.center(30, '-'))  # Output: -------Hello, World!-------




# 4. count()
print(string.count('o'))  # Output: 2 (counts occurrences of 'o')




# 5. encode()   
print(string.encode('utf-8'))  # Output: b'Hello, World!' (encodes the string to bytes using UTF-8 encoding)





