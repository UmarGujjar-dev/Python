# escape sequence


# new line chreacter \n used to create a new line   
print("Hello World \nthis is a new line \nand ofcourse not the last ")

# tab character \t used to create a tab space
print("Hello World \tthis is a tab space \tand ofcourse not the last ")

# backslash character \\ used to create a backslash
print("Hello World \\this is a backslash \\and ofcourse not the last ")

# single quote character \' used to create a single quote
print("Hello World \'this is a single quote \'and ofcourse not the last ")

# double quote character \" used to create a double quote
print("Hello World \"this is a double quote \"and ofcourse not the last ")

# backspace character \b used to create a backspace
print("Hello World \bthis is a backspace \band ofcourse not the last ")

# carriage return character \r used to create a carriage return
print("Hello World \rthis is a carriage return \rand ofcourse not the last ")

# tilde character \~ used to create a tilde
print( "Hello World" , 6 , 7 , sep="~", end="~\n")

